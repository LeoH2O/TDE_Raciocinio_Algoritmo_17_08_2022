# Exercício 4

# Entrada de dados
diametro_cir = float(input("Digite o valor do diâmetro do círculo: "))

# Calculo do raio
raio_cir = diametro_cir / 2
pi = 3.14

# Calculo da area do circulo
area_cir = pi * raio_cir **2

# Mostrar valor da área do circulo
print("Área do círculo é: " + str(area_cir))
