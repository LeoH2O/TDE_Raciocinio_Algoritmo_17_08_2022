# Entrada de dados
salario_func = float(input("Digite o salário do funcionário: R$"))

salario_min = 998

# Calculo do número de salários
salario_num = salario_func / salario_min

# Mostrar quantidade de salários
print("Este funcionário ganha: " + str(salario_num) + " salário(s) mínimo(s)")
